// 전체 평탄화(중복 제거) — 매칭/진단용
export const CRISIS_KEYWORDS_FLAT: string[] = Array.from(new Set(Object.values(CRISIS_KEYWORDS).flat()));

// 프롬프트 삽입용: "카테고리: kw, kw, ..." 여러 줄
export function crisisKeywordsForPrompt(): string {
  return Object.entries(CRISIS_KEYWORDS)
    .map(([cat, kws]) => `  - ${cat}: ${kws.join(', ')}`)
    .join('\n');
}
